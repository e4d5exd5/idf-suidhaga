import { DataTypes, Model } from "sequelize";
import sq from '@/lib/db'
import { ForeignKey } from "sequelize-typescript";
import { UserAuth } from "@/models/User";

export class Bugs extends Model {

}

Bugs.init({
    id: {
        type: DataTypes.STRING(255),
        primaryKey: true,
        unique: true,
        allowNull: false
    },
    ownerId: {
        type: DataTypes.STRING(255),
        references: {
            model: UserAuth,
            key: 'id'
        }
    },
    title: {
        type: DataTypes.TEXT,
        allowNull: false
    },
    description: {
        type: DataTypes.TEXT,
        allowNull: true
    },
}, {
    sequelize: sq, paranoid: true, timestamps: false, modelName: 'Bugs',
    tableName: 'bugs'
})


